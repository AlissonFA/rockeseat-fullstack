let balance = 500 // Saldo da conta
let payment = 120 // Valor do pagamento a ser feito
// Queremos verificar se o saldo é suficiente pra pagar ou não

// > Maior que
console.log(balance > payment) // Saldo maior que o pagamento?

// < Menor que
console.log(balance < payment) // Saldo menor que o pagamento?

// Maior ou igual
balance = 120
console.log(balance >= payment)

// Menor ou igual
balance = 500
console.log(balance <= payment)