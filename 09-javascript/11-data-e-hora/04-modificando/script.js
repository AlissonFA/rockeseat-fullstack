let date = new Date("Jun 8, 2026 14:00:00")

// Modificar o ano.
date.setFullYear(2030)

// Modificar o mês (começa com 0).
date.setMonth(8)

// Modificar o dia.
date.setDate(10)

// Modificar a hora.
date.setHours(18)

// Modificar o minuto.
date.setMinutes(45)

// Modificar o segundo.
date.setSeconds(30)

console.log(date)