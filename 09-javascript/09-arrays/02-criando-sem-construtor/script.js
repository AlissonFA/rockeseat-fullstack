// Criando e acessando um Array sem construtor
let fruits = ["Apple", "Banana", "Orange", "Watermelon"];
console.log(fruits)

// Quantidade de itens do Array
console.log(fruits.length)

// Acessando o item pelo índice
console.log(fruits[1])

// Tentando acessar um índice que não existe
console.log(fruits[7])

// Obtém dinamicamente o último item
console.log(fruits[fruits.length - 1])